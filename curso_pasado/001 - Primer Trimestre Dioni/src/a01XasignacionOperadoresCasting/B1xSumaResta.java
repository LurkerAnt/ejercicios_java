package a01XasignacionOperadoresCasting;

/**
 * 1. Declarar dos variables de tipo entero (int) con valores 15 y 6. Sumarlas y
 * restar 1 al resultado, almacenándolo en una tercera variable. Imprimir el
 * resultado almacenado en esa variable por la salida estándar.
 * 
 * @author Dionisio Fernández Ortiz.
 *
 */

public class B1xSumaResta {
	public static void main(String[] Args) {
		// Declaración de variables
		int numero1, numero2, resultado;
		// Asignación de valores
		numero1 = 15;
		numero2 = 6;
		// Obtención del resultado
		resultado = (numero1 + numero2) - 1;
		// Salida por pantalla
		System.out.println("El resultado es " + resultado);
	}
}
